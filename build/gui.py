def copyQ(number):
    global html
    if html != '':
        soup = BeautifulSoup(html, 'lxml')
        div_next = soup.find('div', id='__next')
        if div_next:
            div_internal = div_next.find('div', {'data-testid': 'questions-template'})
            if div_internal:
                
                wrapper = div_internal.find('div',{'data-testid': "wrapper-Exam" })
                
                if wrapper:
                    q = wrapper.find('div', id=number)
                    title = question(q)
                    pyperclip.copy(title)
                    print(f'question {number} copied!')
                else:
                    print("wrapper não encontrado")
            else:
                print("data-testid='questions-template' não encontrada!")
        else:
            print("Div com id='__next' não encontrada.")
    else:
        print('EMPTY')

